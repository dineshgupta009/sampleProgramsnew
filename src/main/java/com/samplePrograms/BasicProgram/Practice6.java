package com.samplePrograms.BasicProgram;

public class Practice6 {
// find sum of all natural number
    public static void main(String[] args) {

        int n=10 ,sum = 0 ;

        for(int i = 0;i<=n;i++){
            sum = sum +i;
        }
        System.out.println("Sum Of n Natural number : " + sum);
    }
}
